    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
      
      total=0;
      
      int dia =c.get(Calendar.DAY_OF_MONTH);
      Sala salas =(Sala) Cb_salas.getSelectedItem();
      
      
     
        
      quant1=Integer.parseInt(integral.getValue().toString());
      quant2=Integer.parseInt(meia.getValue().toString());
        
      Filme filmes =(Filme) Cb_filmes.getSelectedItem();
      Sessao sessoes =(Sessao) Cb_horario.getSelectedItem();
      
      Double preco = filmes.getPreco();
      
    
                 
            //valor da entrada
            if((Integer.parseInt(integral.getValue().toString())) > 0)
            {
                
              total+= preco*quant1;
              
              
                
            }
             if((Integer.parseInt(meia.getValue().toString())) > 0)
            {
                total+=(preco/2)*quant2;
                
                
           }
           
           
            
            
            setRegistro("Pre√ßo da entrada:R$ " +filmes.getPreco()+"\nSess√£o: "+sessoes.getHorario()+" Dia: "+dia+"\nInteira: "+quant1+" Meia:"+quant2+"\n"+salas.getNome()+"\nTotal:R$ "+total+
                    "\nHorario da compra: "+dtHora.format(data)+"\n");
            
            
        lugar_vend= quant1+quant2;
        switch (Cb_salas.getSelectedIndex()) 
        {
            case 0:
                if((sala1+lugar_vend)<=salas.getCapacidade())
                {
                    
                    for(int i=0;i<lugar_vend;i++)
                    {
                        
                        
                        sala1+=1;
                        cadeira+=" " +Integer.toString(sala1);
                        
                        
                    }
                    setRegistro(getRegistro()+"Cadeiras: "+cadeira);
                    
                    
                }
           else
                {
                    
                    JOptionPane.showMessageDialog(null,"sala cheia");
                    
                    
                    
                }
                break;
            case 1:
                if((sala2+lugar_vend)<=salas.getCapacidade())
                {
                    
                    for(int i=0;i<lugar_vend;i++)
                    {
                        
                        
                        sala2+=1;
                        cadeira+=" " +Integer.toString(sala2);
                        
                        
                    }
                    setRegistro(getRegistro()+"Cadeiras: "+cadeira);
                    
                   
                }
                else
                {
                    
                    JOptionPane.showMessageDialog(null,"sala cheia");
                    
                    
                    
                }
                break;
            case 2:
                if((sala3+lugar_vend)<=salas.getCapacidade())
                {
                    
                    for(int i=0;i<lugar_vend;i++)
                    {
                        
                        
                        sala3+=1;
                        cadeira+=" " +Integer.toString(sala3);
                        
                        
                    }
                    setRegistro(getRegistro()+"Cadeiras: "+cadeira);
                    
                    
                }
                else
                {
                    
                    JOptionPane.showMessageDialog(null,"sala cheia");
                    
                    
                    
                }
                break;
            default:
                break;
        };
        
        extrato.setText("Filme:"+filmes.getNome()+getRegistro());
        
    
        
        
    }//GEN-LAST:event_CalcularActionPerformed

   
